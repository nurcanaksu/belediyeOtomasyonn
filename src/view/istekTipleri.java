    private void secbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_secbtnActionPerformed
        int tablo1satir = tbl.getSelectedRow();
        istektipitxt.setText(String.valueOf(tbl.getValueAt(tablo1satir, 0)));
        String istekadi;
        istekadi = istektipitxt.getText();
        int id = 0;
        try {
            Statement st = this.getDb().connect().createStatement();
            ResultSet rst = st.executeQuery("select * from istek_tipleri");
            while (rst.next()) {
                if (rst.getString("istek_tipi").equals(istekadi)) {
                    id = rst.getInt("istek_tipi_id");
                }
            }
        } catch (Exception e) {
            e.getMessage();
        }
        idsi = id;
    }//GEN-LAST:event_secbtnActionPerformed

    private void eklebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eklebtnActionPerformed
        
         int sayac = 0;
        try {
            Statement st = this.getDb().connect().createStatement();
            ResultSet rst = st.executeQuery("select * from istek_tipleri");
            while (rst.next()) {
                if (istektipitxt.getText().equals(rst.getString("istek_tipi"))) {
                    JOptionPane.showMessageDialog(null, "Bu istek tipi zaten mevcut");
                    sayac++;
                }
            }
            if (sayac != 1) {
                st.executeUpdate("insert into istek_tipleri(istek_tipi) values ('"
                    + istektipitxt.getText() + "')");
                tabloyudoldur();
                lbl.setText("İstek tipi başarılı bir şekilde eklendi");
            }
        } catch (Exception e) {
            e.getMessage();
        }
        
    }//GEN-LAST:event_eklebtnActionPerformed

