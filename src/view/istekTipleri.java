    public ArrayList<IstekTipleri> getIstekTipleri() throws SQLException {
        Connection connection = null;
        DBConnection db = new DBConnection();
        Statement statement = null;
        ResultSet resultSet;
        ArrayList<IstekTipleri> kayitlar = null;

        try {
            connection = db.connect();
            statement = connection.createStatement();
            resultSet = statement.executeQuery("select * from istek_tipleri");
            kayitlar = new ArrayList<IstekTipleri>();

            while (resultSet.next()) {
                kayitlar.add(new IstekTipleri(
                        resultSet.getString("istek_tipi")
                ));
            }

        } catch (SQLException exception) {

        } finally {
            statement.close();
            connection.close();
        }
        return kayitlar;

    }

