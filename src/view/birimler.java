    private void secbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_secbtnActionPerformed
        int tablo1satir = tbl.getSelectedRow();
        birimaditxt.setText(String.valueOf(tbl.getValueAt(tablo1satir, 0)));
        String birimadi;
        birimadi = birimaditxt.getText();
        int id = 0;
        try {
            Statement st = this.getDb().connect().createStatement();
            ResultSet rst = st.executeQuery("select * from birimler");
            while (rst.next()) {
                if (rst.getString("birim_adi").equals(birimadi)) {
                    id = rst.getInt("birim_id");
                }
            }
        } catch (Exception e) {
            e.getMessage();
        }
        idsi = id;
    }//GEN-LAST:event_secbtnActionPerformed

