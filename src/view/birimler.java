    public void tabloyudoldur() {
        model = (DefaultTableModel) tbl.getModel();
        model.setRowCount(0);
        try {
            ArrayList<Birimler> kayitlar = getBirimler();
            for (Birimler rnd : kayitlar) {
                Object[] row = {rnd.getBirim_adi()};
                model.addRow(row);
            }
        } catch (SQLException ex) {

        }
    }

    public ArrayList<Birimler> getBirimler() throws SQLException {
        Connection connection = null;
        DBConnection db = new DBConnection();
        Statement statement = null;
        ResultSet resultSet;
        ArrayList<Birimler> kayitlar = null;

        try {
            connection = db.connect();
            statement = connection.createStatement();
            resultSet = statement.executeQuery("select * from birimler");
            kayitlar = new ArrayList<Birimler>();

            while (resultSet.next()) {
                kayitlar.add(new Birimler(
                        resultSet.getString("birim_adi")
                ));
            }

        } catch (SQLException exception) {

        } finally {
            statement.close();
            connection.close();
        }
        return kayitlar;

    }

    private void eklebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eklebtnActionPerformed
        int sayac = 0;
        try {
            Statement st = this.getDb().connect().createStatement();
            ResultSet rst = st.executeQuery("select * from birimler");
            while (rst.next()) {
                if (birimaditxt.getText().equals(rst.getString("birim_adi"))) {
                    JOptionPane.showMessageDialog(null, "Bu birim zaten mevcut");
                    sayac++;
                }
            }
            if (sayac != 1) {
                st.executeUpdate("insert into birimler(birim_adi) values ('"
                        + birimaditxt.getText() + "')");
                tabloyudoldur();
                lbl.setText("Birim başarılı bir şekilde eklendi");
            }
        } catch (Exception e) {
            e.getMessage();
        }
    }//GEN-LAST:event_eklebtnActionPerformed

